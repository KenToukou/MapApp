# 各都市の需要 (commodity) をランダム生成し、東京(デポ)のみ需要を0とする
demands = []
for city in world_cities["都市名"]:
    if city == "東京":
        demands.append(0)  # デポなので需要を0
    else:
        # 1～10のランダムな整数を生成
        demands.append(random.randint(1, 10))

world_cities["commodity"] = demands


